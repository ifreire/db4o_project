package br.com.ifreire.models;

public class Teste
{
	private int num;
	private String str;
	
	public Teste()
	{
		this.num = 0;
		this.str = null;
	}
	
	public int getNum()
	{
		return num;
	}
	
	public void setNum(int num)
	{
		this.num = num;
	}
	
	public String getStr()
	{
		return str;
	}
	
	public void setStr(String str)
	{
		this.str = str;
	}
}